# Combinacion de Tuplas con Diccionarios

# Definición de una Tupla con nombres de clubes
Tupla_2 = ("Milan", "Juventus", "River", "Chelsea")

# Creación de un Diccionario usando los índices de la tupla como llaves
Dicciona_tupl = {
    Tupla_2[0]: "Gattuso", 
    Tupla_2[1]: "Nedved", 
    Tupla_2[2]: "Mora", 
    Tupla_2[3]: "Drogba"
}

print(Dicciona_tupl)




